def to_device(batch, device):
    output = {}
    for k, v in batch.items():
        try:
            if isinstance(v, list):
                output[k] = [_.detach().to(device) for _ in v]
            elif isinstance(v, torch.Tensor):
                output[k] = v.detach().to(device)
            elif v is None:
                continue
        except Exception as e:
            output[k] = v
            print_rank_0("*" * 20)
            print_rank_0(e + "\n")
            print_rank_0("data to cuda error")
            exit(0)
    return output



class MovingAverage:

    def __init__(self):
        self.count = 0
        self.total = 0
        self.mean = 0

    def update(self, num):
        self.total += num
        self.count += 1
        self.mean = self.total / self.count

        return self.mean


