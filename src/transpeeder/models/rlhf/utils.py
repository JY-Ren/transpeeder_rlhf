def get_attn_mask(bs, seq_length):
        """
        Get triangular attention mask.
        """
        # lower triangular attention mask
        mask = torch.tril(torch.ones((bs, seq_length, seq_length))).view(
            bs, 1, seq_length, seq_length
        )   
        # convert to binary
        return mask < 0.5